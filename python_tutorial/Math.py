# python provides set of built in functions to deal with mathamatical works
#  max(), min(), abs() are available in built in
#  for more functionality you can use math library

x=[1,2,3,4]
# maximum
print(max(x))

# minimum
print(min(x))

# absolute value
print(abs(-10))

# factorial

import math
print(math.factorial(5))

# round off number
print(math.ceil(10.6))

# floor off number
print(math.floor(10.6))

# power
print(math.pow(x[3],5))

# square root  
print(math.sqrt(25))

# pi
print(math.pi)

